### Recommendations for writting scripts

### 1. Include a docstring with the purpose of the script and the input/output. 
### 2. Respect structure. Libraries, parameters, and functions come first in the script. 
### 3. Naming conventions : (https://github.com/naming-convention/naming-convention-guides/tree/master/python)
### 4. Be verbose. Comment every step in the code
### 5. Modularize your code. Don't write long blocks of code. Use functions instead and explain each function purpose.

